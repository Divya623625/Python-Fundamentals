# Q2. Take two numbers as input from the user and print their sum, difference, product and quotient.
num1=int(input("Enter the 1st Value: "))
num2=int(input("Enter the 2nd Value: "))
print("The sum of",num1,"and",num2,"is",num1+num2)
print("The difference of",num1,"and",num2,"is",num1-num2)
print("The product of",num1,"and",num2,"is",num1*num2)
print("The quotient of",num1,"and",num2,"is",num1/num2)
# In Python, a function is a reusable block of code that performs a specific task. 
# Functions help you organize code, avoid repetition, and make programs easier to 
# read and maintain.

# Types Of Functions
# Built-in functions:
# These are pre-defined functions provided by Python, so you don’t need to write their code. 
# Examples: print, len, max, min, sum

# User-defined functions:
# These are functions created by the programmer using def to perform a specific task based 
# on requirement. They help in code reuse and better organization of programs.
# Examples: add, greet, calculate, total

